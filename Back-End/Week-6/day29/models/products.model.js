const mongoose = require("mongoose");

//! Define a Schema

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
    min: 0,
  },
  description: {
    type: String,
  },
  inStock: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

//! Create model

const Product = mongoose.model("Product", productSchema);

module.exports = Product;
